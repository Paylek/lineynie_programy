package by.htp.jdk01.main;

public class lin_prog_25 {
	
	 public static void main(String args[]) {
	        
	        double a = 1, b = 6, c = 4, D, x1, x2;
	     
	        D = b * b - 4 * a * c;
	        x1 = (-b + Math.sqrt(D)) / (2 * a);
	        x2 = (-b - Math.sqrt(D)) / (2 * a);
	       
	    System.out.println("x1 = " + x1);
	    System.out.println("x2 = " + x2);
	    
	    }
}
