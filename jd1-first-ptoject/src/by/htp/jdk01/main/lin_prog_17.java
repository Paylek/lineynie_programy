public class lin_prog_17 {
	
public static void main(String args[]) {
        
        double a = 4, b = 9, c, d;
        
        c = (a * Math.sqrt(a) + b * Math.sqrt(b)) / 2;
        d = Math.sqrt(Math.abs(a * b));
      
        System.out.println("c = " + c);
        System.out.println("d = " + d);
        
    }
}
