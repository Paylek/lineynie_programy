package by.htp.jdk01.main;

public class lin_prog_19 {
	
public static void main(String args[]) {
        
        double a = 4, h, S, r, R;
        
        h = Math.sqrt(3) / 2 * a;
        S = a * h / 2;
        r = a / (2 * Math.sqrt(3));
        R = a / Math.sqrt(3);
       
        System.out.println("h = " + h);
        System.out.println("S = " + S);
        System.out.println("r = " + r);
        System.out.println("R = " + R);
        
    }
}
